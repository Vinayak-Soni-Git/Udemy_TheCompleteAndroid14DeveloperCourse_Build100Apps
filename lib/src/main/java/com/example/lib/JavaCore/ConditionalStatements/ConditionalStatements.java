package com.example.lib.JavaCore.ConditionalStatements;

public class ConditionalStatements {
    public static void main(String[] args) {
        // if statement
        int age = 18;
        if (age >= 18){
            System.out.println("You are an adult");
        }

        // if-else statement
        if (age >= 18){
            System.out.println("You are an adult");
        } else {
            System.out.println("You are minor");
        }
    }
}
